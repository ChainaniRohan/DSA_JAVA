package javaAC_Basics1;

public class dataType {
    public static void main(String[] args) {
        //primitive Datatype!
        byte b = 3;
        System.out.println(b+" byte : 1 byte");
        boolean bool = true;
        System.out.println(bool+" boolean : 1 byte");
        long ln =  345678;
        System.out.println(ln+" long : 8 byte");
        double dbl = 23456.4567;
        System.out.println(dbl+" double : 8 byte");
        short sh = 345;
        System.out.println(sh+" short : 2 byte");
        char ch = 'r';
        System.out.println(ch+" char : 1 byte");
        float price = 10.5f;
        System.out.println(price+" price : 4 byte");
        int i = 34;
        System.out.println(i+" int : 4 byte");

    }
}
